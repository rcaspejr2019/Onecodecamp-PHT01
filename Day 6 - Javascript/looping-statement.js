//Control Flow Structure
// Looping statement

//For loop - we can't control the loop
for (let i = 1; i <= 5; i++) {
console.log("For loop count:" , i);

}

// While loop - can control the loop
 let count = 1;
 while (count <= 5) {
    count++;
    console.log("While loop count:", count);
 }

 //Do while
let a = 10;
do {
    console.log("Do while count:", a)
    a++
} while (a <= 5);



